  <h6>Data ostatniej modyfikacji: 12:01 2005-02-17</h6>
  
  <p>W Gadu-Gadu bardzo prosto można było zmienić swoje dane publiczne lub przejrzeć czyjeś. W Konnekcie jest to równie proste, jednak te same opcje kryją się gdzie indziej. Ten poradnik pokaże jak usprawnić umiejętność korzystania z katalogu publicznego.</p>
  
  <h3 id="step1">Krok 1</h3>
  <img src="pic/katalog/screen_a.png" alt="" class="left" />
  <p>Wybierzcie ma liście kontaktów ofiarę i kliknijcie prawym klawiszem, aby wyświetlić menu kontekstowe. Wybierz <span class="menu">Więcej</span>. Możecie także kliknąć z okna rozmowy na odpowiednią ikonkę jeśli nie dodaliście .</p>
  
  <h3 id="step2">Krok 2</h3>
  <img src="pic/katalog/screen_b.png" alt="" class="left" />
  <p>Wyświetli się okno podsumowania kontaktu. Możesz tam edytować informacje o kontakcie, lecz nam głównie chodzi o wybór z <span class="tool">Odśwież</span> opcji <span class="menu">Katalog publiczny</span>.</p>
	
	<h3 id="step3">Krok 3</h3>
  <img src="pic/katalog/screen_b2.png" alt="" /><img src="pic/katalog/screen_b3.png" alt="" />
  <p>Kontakty Jabbera w menu <span class="tool">Odśwież</span> będą miały opcję <span class="menu">Pobierz vCard (Jabber)</span> a Tlena <span class="menu">Pobierz wizytówkę (Tlen)</span>.</p>
  
  <h3 id="step4">Krok 4</h3>
  <img src="pic/katalog/screen_c.png" alt="" class="left" />
  <p>Wyświetli się okno każące Ci czekać na pobranie informacji. Nie jest to zwykle długie oczekiwanie.</p>
  
  <h3 id="step5">Krok 5</h3>
  <img src="pic/katalog/screen_d.png" alt="" class="left" />
  <p>W oknie wyświetlą się wszystkie dane, jakie kontakt udostępnił w katalogu publicznym. W naszym przypadku to imię, ksywka i wiek.</p>
  
  <h3 id="step6">Krok 6</h3>
  <p>Jeśli chcesz podejrzeć dane użytkowników Tlena robisz tak samo, tylko klikasz <span class="menu">Pobierz wizytówkę (Tlen)</span>. W przypadku Jabbera masz opcję <span class="menu">Pobierz vCard</span>. Wszystko tak samo prosto.</p>
  
  <h3 id="step7">Krok 7</h3>
  <img src="pic/katalog/screen_e.png" alt="" class="left" />
  <p>Jeśli zastanawiasz się jak ustawić sobie dane w katalogu, śpieszymy z pomocą. Wybierz w <span class="tool">Ustawienia</span> -> <span class="menu">Ustawienia</span> pozycję <span class="list">Użytkownik</span>.</p>
  
  <h3 id="step8">Krok 8</h3>
  <img src="pic/katalog/screen_f.png" alt="" class="left" />
	<img src="pic/katalog/screen_f2.png" alt="" class="right" />
  <p>Kliknij na przycisk <span class="but">Ustaw swoje dane personalne</span>. Możesz także od razu kliknąć w menu <span class="tool">Ustawienia</span> i wybrać pozycję <span class="menu">Twoje dane</span>.</p>
  
  <h3 id="step9">Krok 9</h3>
  <img src="pic/katalog/screen_g.png" alt="" class="left" />
  <p>Wpisz wszystko co chcesz mieć udostępnione. Po kliknięciu <span class="but">Zapisz</span> informacja zostanie wysłana do wszystkich sieci z jakich korzystasz. Możesz także <a href="jkatalog.html">wypełnić swój profil</a> w <a href="http://ludzie.jabberpl.org/" class="ex">Polskim Katalogu Użytkowników Jabbera</a>.</p>
  
