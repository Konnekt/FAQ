  <h6>Data ostatniej modyfikacji: 23:35 2005-02-12</h6>
  
  <p>Konnekt w prosty sposób udostępnia możliwość podmiany zestawów emotikonek - obrazków w oknie rozmowy symbolizujących emocje. W tym poradniku pokażemy jak usatwić swoje ulubione, jeśli te domyślne nie pasują nam z różnych powodów.</p>
  
  <h3 id="packages">Paczki emotek</h3>
  <p>Po pierwsze, wejdź do katalogu <span class="folder">Gadu-Gadu/emots</span> i skopiuj katalogi <span class="foder">1</span> i <span class="foder">2</span> do katalogu <span class="folder"><a href="sql_folders.html#konnekt">%Konnekt%</a>\emots</span>. Jeśli nie masz zainstalowanego Gadu-Gadu, zawsze możesz ściągnąć <a href="http://www.konnekt.info/katalog/emotikony/857.html" class="ex">gotowy zestaw</a> ze strony oficjalnej Konnekt'a:</p>
  
  <p>Rozpakuj plik <span class="file">GGemots.rar</span> (potrzeba będzie <a href="http://www.rarlab.com/download.htm" class="ex">WinRARa</a> do tego) za pomocą opcji <span class="menu">Wypakuj tutaj</span> (prawym przyciskiem myszy na ikonkę pliku...) następnie przenieś folder <span class="folder">GGemots</span> do folderu <span class="folder"><a href="sql_folders.html#konnekt">%Konnekt%</a>\emots</span></p>
  
  <h3 id="config">Konfiguracja</h3>
  <img src="pic/emots/screen_a.png" alt="" class="left" />
  <p>Wejdź do <span class="menu">Konfiguracji</span>, rozwiń z drzewka po lewej opcję <span class="list">Okno rozmowy</span> i przejdź do <span class="list">Emotikony</span>.</p>
  
  <h3 id="sets">Wybór zestawów</h3>
  <img src="pic/emots/screen_b.png" alt="" class="left" />
  <p>W prawej części okna pojawi się dosyć dużo opcji, ale nie ma się czym przejmować. Z podanych na obrazku najważniejsza jest <span class="chxon">Wyświetlaj emotikony</span> - musisz ją włączyć, żeby móc oglądać jakiekolwiek emotki. Opcja <span class="chx on">Dopuszczaj lokalne zestawy emotikon (dla kontaktu)</span> znaczy, że możesz dla kogoś wyjątkowego ustawić inny zestaw emotek, o których zaraz będzie mowa. Opcja trzecia (<span class="chx on">Wyświetlaj emotikony w historii</span>), jeśli jeszcze Cię to interesuje, pozwala na pokazywanie emotek w historii rozmów, tak jak domyślnie jest w Gadu-Gadu. Jednak wpływa to ujemnie na czas ładowania się rozmowy, więc jeśli masz wolny komputer, lepiej odznacz to pole. :)</p>
  
  <h3 id="changes">Zmiany</h3>
  <img src="pic/emots/screen_d.png" alt="" class="left" />
  <img src="pic/emots/screen_d2.png" alt="" class="right" />
  <p>Zmiany możesz zaobserwować już podczas rozmowy z którymś ze znajomych - spróbuj używać emotikonek, wpisując je z klawiatury, lub wybierając przez menu emotikonek, co jest pokazane po prawej.</p>
  
  <h3 id="moresets">Inne zestawy</h3>
  <img src="pic/emots/screen_e.png" alt="" class="left" />
  <p>Możesz także ściągnąć <a href="http://www.konnekt.info/katalog/emotikony.html" class="ex">więcej zestawów z konnekt.info</a>, analogicznie je rozpakować do <span class="folder"><a href="sql_folders.html#konnekt">%Konnekt%</a>\emots</span>, a potem w <span class="menu">Konfiguracji</span> -> <span class="list">Okno rozmowy</span> -> <span class="list">Emotikony</span> wybrać zestawy oznaczone <span class="input off">(nowy)</span>.</p>
  
  <p>Pamiętaj, że najlepiej wybierać jeden zestaw, chyba, że jest zaznaczone, że nie koliduje on z zestawem Gadu-Gadu. W przeciwnym wypadku część emotek będzie z jednego zestawu a część z drugiego, co nie wygląda za ładnie. Odznacz więc zestawy <span class="input off">1</span> i <span class="input off">2</span> aby podziwiać resztę:</p>
    
