  <p>Knotify to wtyczka, która pokazuje nam powiadomienia o zmianach statusów, o tym, że ktoś nam przysyła wiadomość w formie okienka nad paskiem Start, nad zegarkiem systemowym. Może także wyświetlać inne pożyteczne informacje, np.: powiadomienia od innych wtyczek etc. Bardzo użyteczną opcją wtyczki jest to, że przychodzące wiadomości układają się w stos, jedna na drugiej, a nie na sobie, jak to miało miejsce w Gadu-Gadu.</p>
  
